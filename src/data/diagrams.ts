import { MarkerType, type Edge, type Node } from '@xyflow/react'
import type { DiagramDefinition } from '../types/course'

type DiagramNodeInput = {
  id: string
  label: string
  x: number
  y: number
  tone?: 'agent' | 'human' | 'tool' | 'policy' | 'memory' | 'artifact' | 'risk'
  width?: number
}

const toneStyles = {
  agent: { border: '#4e9fb0', background: '#22313c' },
  human: { border: '#6f8aa5', background: '#242d3a' },
  tool: { border: '#5b9a79', background: '#23342f' },
  policy: { border: '#b99857', background: '#373021' },
  memory: { border: '#8d80b6', background: '#302b3f' },
  artifact: { border: '#6f8aa5', background: '#222a35' },
  risk: { border: '#bf6b6b', background: '#3a292b' },
} as const

function node(input: DiagramNodeInput): Node {
  const tone = toneStyles[input.tone ?? 'artifact']
  return {
    id: input.id,
    position: { x: input.x, y: input.y },
    data: { label: input.label },
    style: {
      width: input.width ?? 170,
      minHeight: 54,
      border: `1px solid ${tone.border}`,
      borderRadius: 8,
      background: tone.background,
      color: '#edf1f5',
      fontSize: 13,
      lineHeight: 1.25,
      padding: 12,
      textAlign: 'center',
    },
  }
}

function edge(source: string, target: string, label?: string): Edge {
  return {
    id: `${source}-${target}-${label ?? 'flow'}`,
    source,
    target,
    label,
    markerEnd: { type: MarkerType.ArrowClosed, color: '#7e8da0' },
    style: { stroke: '#7e8da0', strokeWidth: 1.6 },
    labelStyle: { fill: '#d7dee7', fontSize: 11 },
    labelBgStyle: { fill: '#1b222c', fillOpacity: 0.92 },
  }
}

function diagram(input: DiagramDefinition): DiagramDefinition {
  return input
}

